#include "../libs/so_long.h"

int	ft_strcmp(const char s1, const char *s2)
{
	size_t	i;

	i = 0;
	while (s2[i])
	{
		if (s1 == s2[i])
			return (0);
		i++;
	}
	return (1);
}
